const SUB = '₀₁₂₃₄₅₆₇₈₉'
function sub(n: number): string {
  return String(n)
    .split('')
    .map((d) => SUB[Number(d)] ?? d)
    .join('')
}

function wrapCount(u: string, n: number): string {
  if (n === 1) return u
  if (u.length === 1 || !u.includes('(')) return u + sub(n)
  return `(${u})${sub(n)}`
}

